x = 5 # Binary: 0101
y = 3 # Binary: 0011
print("Bitwise And = ",x & y) # Bitwise AND: 1 (0001)
print("Bitwise OR = ",x | y) # Bitwise OR: 7 (0111)
print("Bitwise XOR = ",x ^ y) # Bitwise XOR: 6 (0110)
print("Bitwise Not on X = ",~x) # Bitwise NOT: -6 (1010 in 2's complement form)
# print("Left Shift of X = ", x << 1) # Left Shift: 10 (1010)
print("Right Shift of X = ",x >> 1) # Right Shift: 2 (0010)